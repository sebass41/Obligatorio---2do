
package logica;


public class Cliente {
    private int cedula, telefono, codPostal;
    private String nombre, apellido, empresa, direccion, ciudad, departamento;

    public Cliente(int cedula, int telefono, int codPostal, String nombre, String apellido, String empresa, String direccion, String ciudad, String departamento) {
        this.cedula = cedula;
        this.telefono = telefono;
        this.codPostal = codPostal;
        this.nombre = nombre;
        this.apellido = apellido;
        this.empresa = empresa;
        this.direccion = direccion;
        this.ciudad = ciudad;
        this.departamento = departamento;
    }

    public int getCedula() {
        return cedula;
    }

    public void setCedula(int cedula) {
        this.cedula = cedula;
    }

    public int getTelefono() {
        return telefono;
    }

    public void setTelefono(int telefono) {
        this.telefono = telefono;
    }

    public int getCodPostal() {
        return codPostal;
    }

    public void setCodPostal(int codPostal) {
        this.codPostal = codPostal;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getEmpresa() {
        return empresa;
    }

    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public String getDepartamento() {
        return departamento;
    }

    public void setDepartamento(String departamento) {
        this.departamento = departamento;
    }
    
    
}
